int plus(int x)
{
  return +x;
} 
 
int minus(int x)
{
  return -x;
}

int add(int x, int y)
{
  return x + y;
}

int sub(int x, int y)
{
  return x - y;
}

int mul(int x, int y)
{
  return x * y;
}

int div(int x, int y)
{
  return x / y;
}

int or(int x, int y)
{
  return x || y;
}

int and(int x, int y)
{
  return x && y;
}

int eq(int x, int y)
{
  return x == y;
}

int ne(int x, int y)
{
  return x != y;
}

int lt(int x, int y)
{
  return x < y;
}

int gt(int x, int y)
{
  return x > y;
}

int le(int x, int y)
{
  return x <= y;
}

int ge(int x, int y)
{
  return x >= y;
}
